import Foundation
import SwiftUI

extension Color {
    static let theme = ColorTheme()
}

struct ColorTheme {
    let blackColor = Color("BlackColor")
    let darkGreyColor = Color("DarkGreyColor")
    let greyColor = Color("GreyColor")
    let mainBlueColor = Color("MainBlueColor")
    let mainPurpleColor = Color("MainPurpleColor")
    let warningRedColor = Color("WarningRedColor")
    let whiteColor = Color("WhiteColor")
    let whiteGreyColor = Color("WhiteGreyColor")
}
